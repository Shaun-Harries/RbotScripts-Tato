using System;
using System.Collections.Generic;
using RBot;
using RBot.Options;
public class Script
{


    public void ScriptMain(ScriptInterface bot)
    {
        bot.Options.SafeTimings = true;
        bot.Options.RestPackets = true;
        bot.Options.ExitCombatBeforeQuest = true;
        bot.Options.InfiniteRange = true;
        bot.Options.SkipCutscenes = true;
        
        bot.Skills.Add( 1, 1 );
		bot.Skills.Add( 2, 1 );
		bot.Skills.Add( 3, 1 );
		bot.Skills.Add( 4, 1);
		bot.Skills.StartTimer();
		
		bot.Drops.Add("Fiend Token");
		bot.Drops.Add("Dragon Fiend Gem");
		bot.Drops.Add("Glacial Bones");
		bot.Drops.Add("Screamfeeder Heart");
		bot.Drops.Enabled = true;
		
		//this bot doesn't farm Diamonds of Nulgath, because Diamond of Nulgath bot already exists, go prefarm it :P
        
        while(!bot.ShouldExit()) {
        bot.Quests.EnsureAccept(3053);
        
        bot.Player.Join("lair-999999", "End", "Right", false);
		bot.Player.HuntForItem("Red Dragon", "Dragon Fiend Gem", 13, false, true);
        
        bot.Player.Join("battleunderd-999999", "r5", "Left", false);
        while(!bot.Inventory.Contains("Glacial Bones", 3)) {
        bot.Player.UseSkill(1);
        bot.Player.UseSkill(2);
        bot.Player.UseSkill(4);
        bot.Player.RejectExcept("Glacial Bones");
		}
       
        bot.Player.Join("dreammaze-999999");
       	 bot.Sleep(2500);
        bot.Player.Jump("r39" , "Right");
        bot.Player.HuntForItem("Screamfeeder", "Screamfeeder Heart", 1, false, true);
      	
        bot.Player.Jump("Blank", "Spawn", false);
        bot.Quests.EnsureComplete(3053, -1, false, 100);
        bot.Player.Pickup("Fiend Token");
        
        }
    }
}
