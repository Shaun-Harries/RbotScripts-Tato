public class Script
{
    //                 player name here â†“
    public string playerName = "Username"; //ðŸ¥”
    public int Test = 25; // Loop amount

    public void ScriptMain(ScriptInterface bot)
    {
        bot.Options.SafeTimings = true;
        //bot.Options.LagKiller = true;
        bot.Options.DisableFX = true;
        bot.Options.InfiniteRange = true;
        bot.Options.SkipCutscenes = true;
        bot.Options.CustomName = "Bot by Jex"; // ye this guy
        bot.Options.LoadTimeout = 5000;
        bot.Skills.StartTimer();
        
        
        while (!bot.ShouldExit())
        {
            if (!bot.Map.PlayerExists(playerName)) { Teleport(bot); }
            if (bot.Map.GetPlayer(playerName) != null && bot.Map.GetPlayer(playerName).Cell != bot.Player.Cell)
            {
                bot.Player.Jump(bot.Map.GetPlayer(playerName).Cell, bot.Map.GetPlayer(playerName).Pad);
            }
            if (bot.Monsters.CurrentMonsters.Count(m => m.Alive) > 0) { Attack(bot); }
            bot.Log("I'm in PR_F");
            bot.Sleep(1000);
        }
    }

    public void Teleport(ScriptInterface bot)
    {
        bot.Player.Jump("wait", "Spawn");
        bot.Sleep(500);
        bot.Player.Goto(playerName);
        bot.Sleep(1000);
        bot.Log("I'm in TP_F");
    }

    public void Attack(ScriptInterface bot)
    {
        bot.Log("I'm in ATT_F");
        for (var i = 0; i < Test; i++)
        {
            bot.Player.UseSkill(3);
            bot.Player.UseSkill(2);
            bot.Player.UseSkill(1);
            bot.Player.UseSkill(4);
        }
    }
