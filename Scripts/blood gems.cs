using System;
using System.Collections.Generic;
using RBot;
using RBot.Options;
public class Script
{
    public string OptionsStorage = "Author_v2 Fastest Kiss the Void (Blood Gems of The Archfiend)";
    public bool DontPreconfigure = true;


    public void ScriptMain(ScriptInterface bot)
    {
        bot.Options.SafeTimings = true;
        bot.Options.RestPackets = true;
        bot.Options.ExitCombatBeforeQuest = true;
        bot.Options.InfiniteRange = true;
        bot.Options.PrivateRooms = true;
        bot.Options.LagKiller = true;
        bot.Options.HuntDelay = 0;

        bot.Skills.StartSkills("Skills/Generic.xml");
        
        bot.Player.LoadBank();
        bot.Bank.ToInventory("Tendurrr The Assistant");
        bot.Bank.ToInventory("Blood Gem of the Archfiend");
      	bot.Bank.ToInventory("Fragment of Chaos");

        bot.Quests.EnsureAccept(3743);

        bot.Player.Join("citadel-1e9", "Enter", "Spawn");
        bot.Sleep(500);
        bot.Player.Jump("m22", "Spawn");
        bot.Player.Join("tercessuinotlim-1e9", "Enter", "Spawn");
        bot.Sleep(500);
        bot.Player.HuntForItem("Dark Makai", "Tendurrr The Assistant", 1, false, true);
        if (!bot.Inventory.Contains("Tendurrr The Assistant", 1)) 
        bot.Player.Jump("blank", "spawn");
        

        bot.Player.Join("lair-1e9", "Enter", "Spawn");
        bot.Player.HuntForItem("Water Draconian", "Fragment of Chaos", 80, false, true);
        

        bot.Player.Join("underworld-1e9", "r13", "Left");
        bot.Player.HuntForItem("Legion Fenrir", "Broken Betrayal Blade", 8, true, true);
        
        
        bot.Player.Join("battleon-1e9", "Enter", "Spawn");
        bot.Quests.EnsureComplete(3743, -1);
        bot.Wait.ForDrop("Blood Gem of the Archfiend");
        bot.Player.Pickup("Blood Gem of the Archfiend");
        bot.Sleep(1000);
        if (!bot.Inventory.Contains("Blood Gem of the Archfiend", 100)) 
        {

        }
    }
}

